ALTER TABLE POKEMNON ADD CONSTRAINT FK_POKEMNON_TRAINER_ID FOREIGN KEY (TRAINER_ID) REFERENCES TRAINER (ID);
ALTER TABLE POKEMNON_TYPE ADD CONSTRAINT FK_POKEMNON_TYPE_TYPE_ID FOREIGN KEY (TYPE_ID) REFERENCES TYPE (ID);
