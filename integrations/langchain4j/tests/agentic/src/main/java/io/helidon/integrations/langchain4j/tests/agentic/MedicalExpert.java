package io.helidon.integrations.langchain4j.tests.agentic;

import io.helidon.integrations.langchain4j.Ai;

import dev.langchain4j.agentic.Agent;
import dev.langchain4j.service.UserMessage;
import dev.langchain4j.service.V;

/**
 * MedicalExpert defines an AI‑driven medical analysis capability.
 *
 * @see Ai.Agent
 * @see Ai.ChatModel
 */
@Ai.Agent("medical-expert")
@Ai.ChatModel("google-gemini")
public interface MedicalExpert {

    /**
     * Performs a medical analysis of the provided user request using an AI‑driven medical expert.
     *
     * @param request the user request to be examined; may contain any text describing a medical concern or question
     * @return a textual response generated by the AI medical expert that contains the medical analysis and answer
     */
    @UserMessage("""
            You are a medical expert.
            Analyze the following user request under a medical point of view and provide the best possible answer.
            The user request is {{request}}.
            Always start your response with introducing yourself as AI medical expert.
            """)
    @Agent(value = "A medical expert", outputKey = "response")
    String medical(@V("request") String request);
}
