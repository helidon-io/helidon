package io.helidon.microprofile.graphql.server.test.types;

import java.util.Objects;

import org.eclipse.microprofile.graphql.Input;

/**
 * Defines a simple contact input type which contains an address.
 */
@Input
public class SimpleContactInputTypeWithAddress {
    private String id;
    private String name;
    private int age;
    private Address address;

    public SimpleContactInputTypeWithAddress(String id,
                                             String name,
                                             int age,
                                             Address address) {
        this.id = id;
        this.name = name;
        this.age = age;
        this.address = address;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public Address getAddress() {
        return address;
    }

    public void setAddress(Address address) {
        this.address = address;
    }
}
